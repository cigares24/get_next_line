#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		n;
	int		i;

	n = 0;
	while (s1[n] != '\0')
		n++;
	i = 0;
	while (s2[i] != '\0')
	{
		s1[n + i] = s2[i];
		i++;
	}
	s1[n + i] = '\0';
	return (s1);
}
