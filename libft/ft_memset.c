#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		n;
	char	*res;

	res = b;
	n = 0;
	while (len > 0)
	{
		res[n] = c;
		n++;
		len--;
	}
	return (res);
}
