#include "libft.h"

char	*ft_strncat(char *s1, char *s2, size_t n)
{
	int				x;
	int				k;
	unsigned int	i;

	x = 0;
	k = 0;
	while (s1[x] != '\0')
		x++;
	i = 0;
	while (s2[k] != '\0' && i < n)
	{
		s1[x + k] = s2[k];
		k++;
		i++;
	}
	s1[x + k] = '\0';
	return (s1);
}
