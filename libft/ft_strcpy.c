#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		n;

	n = 0;
	while (src[n] != '\0')
	{
		dst[n] = src[n];
		n++;
	}
	dst[n] = '\0';
	return (dst);
}
